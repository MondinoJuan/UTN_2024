USE `sonasatam`;

#
# Structure for the `kinesiologos` table : 
#

DROP TABLE IF EXISTS `kinesiologos`;

CREATE TABLE `kinesiologos` (
  `matricula` int(11) NOT NULL,
  `nombre` varchar(20) NOT NULL,
  `apellido` varchar(20) NOT NULL,
  `telefono` varchar(20) DEFAULT NULL,
  `email` varchar(50) DEFAULT NULL,
  PRIMARY KEY (`matricula`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `agendas` table : 
#

DROP TABLE IF EXISTS `agendas`;

CREATE TABLE `agendas` (
  `matricula` int(11) NOT NULL,
  `fecha_desde` date NOT NULL,
  `dia_semana` int(11) NOT NULL,
  `hora_desde` time NOT NULL,
  `hora_hasta` time NOT NULL,
  PRIMARY KEY (`matricula`,`fecha_desde`,`dia_semana`,`hora_desde`),
  CONSTRAINT `agendas_kinesiologos_fk` FOREIGN KEY (`matricula`) REFERENCES `kinesiologos` (`matricula`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `clientes` table : 
#

DROP TABLE IF EXISTS `clientes`;

CREATE TABLE `clientes` (
  `dni` varchar(20) NOT NULL,
  `nombre` varchar(20) NOT NULL,
  `apellido` varchar(20) NOT NULL,
  `telefono` varchar(20) DEFAULT NULL,
  `domicilio` varchar(50) DEFAULT NULL,
  PRIMARY KEY (`dni`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `materiales` table : 
#

DROP TABLE IF EXISTS `materiales`;

CREATE TABLE `materiales` (
  `cod_mat` int(11) NOT NULL,
  `nom_mat` varchar(50) NOT NULL,
  `cant_disp` int(11) NOT NULL,
  PRIMARY KEY (`cod_mat`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `ventas` table : 
#

DROP TABLE IF EXISTS `ventas`;

CREATE TABLE `ventas` (
  `nro_venta` int(11) NOT NULL,
  `fecha` date NOT NULL,
  `dni` varchar(20) NOT NULL,
  PRIMARY KEY (`nro_venta`),
  KEY `ventas_clientes_fk` (`dni`),
  CONSTRAINT `ventas_clientes_fk` FOREIGN KEY (`dni`) REFERENCES `clientes` (`dni`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `detalle_ventas` table : 
#

DROP TABLE IF EXISTS `detalle_ventas`;

CREATE TABLE `detalle_ventas` (
  `nro_venta` int(11) NOT NULL,
  `cod_mat` int(11) NOT NULL,
  `cantidad` int(11) NOT NULL,
  PRIMARY KEY (`nro_venta`,`cod_mat`),
  KEY `detalle_ventas_materiales_fk` (`cod_mat`),
  CONSTRAINT `detalle_ventas_materiales_fk` FOREIGN KEY (`cod_mat`) REFERENCES `materiales` (`cod_mat`) ON UPDATE CASCADE,
  CONSTRAINT `detalle_ventas_ventas_fk` FOREIGN KEY (`nro_venta`) REFERENCES `ventas` (`nro_venta`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `equipos` table : 
#

DROP TABLE IF EXISTS `equipos`;

CREATE TABLE `equipos` (
  `cod_eq` int(11) NOT NULL,
  `nom_eq` varchar(50) NOT NULL,
  `cant_disp` int(11) NOT NULL,
  PRIMARY KEY (`cod_eq`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `tratamientos` table : 
#

DROP TABLE IF EXISTS `tratamientos`;

CREATE TABLE `tratamientos` (
  `cod_tratamiento` int(11) NOT NULL,
  `desc_tratamiento` varchar(50) NOT NULL,
  `precauciones` varchar(200) DEFAULT NULL,
  `duracion` int(11) NOT NULL,
  PRIMARY KEY (`cod_tratamiento`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `equipos_tratamientos` table : 
#

DROP TABLE IF EXISTS `equipos_tratamientos`;

CREATE TABLE `equipos_tratamientos` (
  `cod_eq` int(11) NOT NULL,
  `cod_tratamiento` int(11) NOT NULL,
  PRIMARY KEY (`cod_eq`,`cod_tratamiento`),
  KEY `equipos_tratamientos_tratamientos_fk` (`cod_tratamiento`),
  CONSTRAINT `equipos_tratamientos_equipos_fk` FOREIGN KEY (`cod_eq`) REFERENCES `equipos` (`cod_eq`) ON UPDATE CASCADE,
  CONSTRAINT `equipos_tratamientos_tratamientos_fk` FOREIGN KEY (`cod_tratamiento`) REFERENCES `tratamientos` (`cod_tratamiento`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `obras_sociales` table : 
#

DROP TABLE IF EXISTS `obras_sociales`;

CREATE TABLE `obras_sociales` (
  `cod_os` int(11) NOT NULL,
  `razon_social` varchar(50) NOT NULL,
  `cuit` varchar(13) NOT NULL,
  `direccion` varchar(50) DEFAULT NULL,
  `telefono` varchar(20) DEFAULT NULL,
  PRIMARY KEY (`cod_os`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `recetas` table : 
#

DROP TABLE IF EXISTS `recetas`;

CREATE TABLE `recetas` (
  `nro_mat` int(11) NOT NULL,
  `nro_receta` int(11) NOT NULL,
  `nom_med` varchar(50) NOT NULL,
  `fecha_receta` date NOT NULL,
  `nom_sanat` varchar(50) NOT NULL,
  `cod_os` int(11) DEFAULT NULL,
  PRIMARY KEY (`nro_mat`,`nro_receta`),
  KEY `recetas_obras_sociales_fk` (`cod_os`),
  CONSTRAINT `recetas_obras_sociales_fk` FOREIGN KEY (`cod_os`) REFERENCES `obras_sociales` (`cod_os`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `tratamientos_clientes` table : 
#

DROP TABLE IF EXISTS `tratamientos_clientes`;

CREATE TABLE `tratamientos_clientes` (
  `dni` varchar(20) NOT NULL,
  `cod_tratamiento` int(11) NOT NULL,
  `fecha` date NOT NULL,
  `hora` time NOT NULL,
  `cant_sesiones` int(11) NOT NULL,
  `nro_mat` int(11) DEFAULT NULL,
  `nro_receta` int(11) DEFAULT NULL,
  PRIMARY KEY (`dni`,`cod_tratamiento`,`fecha`,`hora`),
  KEY `tratamientos_clientes_tratamientos_fk` (`cod_tratamiento`),
  KEY `tratamientos_clientes_recetas_fk` (`nro_mat`,`nro_receta`),
  CONSTRAINT `tratamientos_clientes_clientes_fk` FOREIGN KEY (`dni`) REFERENCES `clientes` (`dni`) ON UPDATE CASCADE,
  CONSTRAINT `tratamientos_clientes_recetas_fk` FOREIGN KEY (`nro_mat`, `nro_receta`) REFERENCES `recetas` (`nro_mat`, `nro_receta`) ON UPDATE CASCADE,
  CONSTRAINT `tratamientos_clientes_tratamientos_fk` FOREIGN KEY (`cod_tratamiento`) REFERENCES `tratamientos` (`cod_tratamiento`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `turnos` table : 
#

DROP TABLE IF EXISTS `turnos`;

CREATE TABLE `turnos` (
  `nro_turno` int(11) NOT NULL,
  `dni` varchar(20) NOT NULL,
  `cod_tratamiento` int(11) NOT NULL,
  `fecha` date NOT NULL,
  `hora` time NOT NULL,
  `matricula` int(11) NOT NULL,
  `fecha_desde` date NOT NULL,
  `dia_semana` int(11) NOT NULL,
  `hora_desde` time NOT NULL,
  `fecha_turno` date NOT NULL,
  `hora_turno` time NOT NULL,
  `estado` varchar(50) NOT NULL,
  PRIMARY KEY (`nro_turno`),
  KEY `turnos_tratamientos_clientes_fk` (`dni`,`cod_tratamiento`,`fecha`,`hora`),
  KEY `turnos_agendas_fk` (`matricula`,`fecha_desde`,`dia_semana`,`hora_desde`),
  CONSTRAINT `turnos_agendas_fk` FOREIGN KEY (`matricula`, `fecha_desde`, `dia_semana`, `hora_desde`) REFERENCES `agendas` (`matricula`, `fecha_desde`, `dia_semana`, `hora_desde`) ON UPDATE CASCADE,
  CONSTRAINT `turnos_tratamientos_clientes_fk` FOREIGN KEY (`dni`, `cod_tratamiento`, `fecha`, `hora`) REFERENCES `tratamientos_clientes` (`dni`, `cod_tratamiento`, `fecha`, `hora`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `materiales_turnos` table : 
#

DROP TABLE IF EXISTS `materiales_turnos`;

CREATE TABLE `materiales_turnos` (
  `cod_mat` int(11) NOT NULL,
  `nro_turno` int(11) NOT NULL,
  `cantidad` decimal(9,3) NOT NULL,
  PRIMARY KEY (`cod_mat`,`nro_turno`),
  KEY `materiales_turnos_turnos_fk` (`nro_turno`),
  CONSTRAINT `materiales_turnos_materiales_fk` FOREIGN KEY (`cod_mat`) REFERENCES `materiales` (`cod_mat`) ON UPDATE CASCADE,
  CONSTRAINT `materiales_turnos_turnos_fk` FOREIGN KEY (`nro_turno`) REFERENCES `turnos` (`nro_turno`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `precios_materiales` table : 
#

DROP TABLE IF EXISTS `precios_materiales`;

CREATE TABLE `precios_materiales` (
  `cod_mat` int(11) NOT NULL,
  `fecha_desde` date NOT NULL,
  `valor_material` decimal(9,3) NOT NULL,
  PRIMARY KEY (`cod_mat`,`fecha_desde`),
  CONSTRAINT `precios_materiales_materiales_fk` FOREIGN KEY (`cod_mat`) REFERENCES `materiales` (`cod_mat`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#
# Structure for the `precios_tratamientos` table : 
#

DROP TABLE IF EXISTS `precios_tratamientos`;

CREATE TABLE `precios_tratamientos` (
  `cod_tratamiento` int(11) NOT NULL,
  `fecha_desde` date NOT NULL,
  `valor_tratamiento` decimal(9,3) NOT NULL,
  PRIMARY KEY (`cod_tratamiento`,`fecha_desde`),
  CONSTRAINT `precios_tratamientos_tratamientos_fk` FOREIGN KEY (`cod_tratamiento`) REFERENCES `tratamientos` (`cod_tratamiento`) ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

